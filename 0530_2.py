##이스케이프 코드를 사용하여 출력 서식 지정 프로그램##
print("="*55)
print("이스케이프 코드로 출력 서식 지정 프로그램")
print("-"*55)
print(">>줄바꿈 코드 \\n \n -> 현재 행에서 다음 행으로 줄 바꿈")
print(">>일정간격 띄우는 탭 \\t \t -> 탭 간격만큼 띄움")
print(">>단일 인용부호 -> \'단일 인용\' 부호")
print(">>이중 인용부호 -> \"이중 인용\" 부호")
print(">>지정 문자 출력 -> 윈(\\)문자 출력")
print(">>Null(\\000) 문자 출력 -> (\000) Null 문자는 안 보임")
print("="*55)
print("\\12345","\'한국\'","\n 대구")
print("10+20")
print("%f" % (10+20))
print("%f %f" % (10.5,20.5))
print("%d" % (10+20))
print("%d %d" % (10.7,20.7))
print("%d" % (10.7+20.7))
print("%d, %d" % (10,20))
print("space",end='')       #다음 문자열 붙이기
print("zone",end='good')

print(">>정수345를 \"10d\" 형식지정 \t", end='')
print(format(345,"10d"))
print(">>정수345를 \"010d\" 형식지정 \t", end='')
print(format(345,"010d"))
print(">>정수345를 \"<10d\" 형식지정 \t", end='')
print(format(345,"<10d"))
print(">>정수345를 \">10d\" 형식지정 \t", end='')
print(format(345,">10d"))
print(">>정수345를 \"10b\" 형식지정 \t", end='')
print(format(345,"10b"))
print(">>정수12345를 \",d\" 형식지정 \t", end='')
print(format(12345,",d"))
